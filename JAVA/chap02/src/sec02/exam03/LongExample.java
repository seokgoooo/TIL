package sec02.exam03;

public class LongExample {
	public static void main(String[] args) {
		long var1 = 10;
		long var2 = 20L;
//		long var3 = 1000000000000; >> long 타입은 수치가 큰 데이터를 다루는 프로그램에서 주로 사용. 은행이나 과학과 관련된 프로그램
		long var4 = 1000000000000L; // 정수 리터럴 뒤에 l 이나 L을 붙여주는데 주로 대문자 L을 사용.
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var4);
	}
}
