public class ExampleOne{
	public static void main(String[] args) {
		int x;
		int y;
		int sum;
		int minus;
		int mul;
		int div;

		x = 44;
		y = 72;

		sum = x + y;
		minus = x - y;
		mul = x * y;
		div = x / y;
				
		System.out.println("44 + 72 : " + sum);
		System.out.println("44 - 72 : " + minus);
		System.out.println("44 X 72 : " + mul);
		System.out.println("44 / 72 : " + div);
	}
}